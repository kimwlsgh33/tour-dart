void main() {
  //================================================================
  // num - parent of int and double
  //================================================================
  num x = 1;
  x = 1.1;

  //================================================================
  // list - array
  //================================================================
  var numbers = [1, 2, 3];
  // numbers[0] = 1.1; // error - num is not int

  List<num> numbers2 = [1, 2, 3]; // for class

  //================================================================
  // collection if
  //================================================================
  var numbers3 = [1, 2, 3, if (true) 4, if (false) 5];
  print(numbers3);

  var name = 'jinho';
  var age = 23;
  // after $, you can use any expression
  var greeting = 'hello $name, you\'re ${age + 2} years old';
  print(greeting);

  //================================================================
  // collection for
  //================================================================
  var numbers4 = [for (var n in numbers3) n * 2];
  print(numbers4);

  //================================================================
  // map - dictionary
  //================================================================
  var player = {
    'name': 'jinho',
    'xp': 19.34,
    'level': 2,
    'items': ['sword', 'shield']
  };

  //================================================================
  // set - no duplicate
  //================================================================
  var items = {'sword', 'shield', 'sword'};
  print(items); // {sword, shield}
}
